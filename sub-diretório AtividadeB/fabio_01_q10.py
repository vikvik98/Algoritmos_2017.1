print ("Informe dois numeros inteiros para que o programa possa dividir o primeiro pelo segundo e informar o quociente e o resto da divisão.")

# Entrada
Primeiro_numero = int (input ("Primeiro numero:"))
Segundo_numero = int (input ("Segundo numero:"))

# Calcule
Quociente = Primeiro_numero / Segundo_numero
Resto = Primeiro_numero % Segundo_numero

# Saída
print ("O quociente é igual a =", Quociente, "e o resto é =", Resto)